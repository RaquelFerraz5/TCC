  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A tireoidite diz respeito a qualquer tipo de inflamação da tireoide. A inflamação da tireoide pode ser causada por uma infecção viral ou uma doença autoimune. 
 </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A tireoidite de Hashimoto é o tipo mais frequente de tireoidite e a causa mais comum de hipotireoidismo. Por razões desconhecidas, o corpo se volta contra si mesmo (reação autoimune). A tireoide é invadida por glóbulos brancos e são criados anticorpos que atacam a tireoide (anticorpos antitireoidianos). A tireoidite diz respeito a qualquer tipo de inflamação da tireoide. A inflamação da tireoide pode ser causada por uma infecção viral ou uma doença autoimune.
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Inicialmente, a inflamação da tireoide provoca uma ruptura que resulta no excesso de hormônios da tireoide (hipertireoidismo). Ao longo do tempo, a inflamação impede a tireoide de produzir hormônios suficientes (hipotireoidismo). Os sintomas incluem fadiga e ganho de peso inexplicável. 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento com medicamentos hormonais sintéticos costuma ser eficaz.</p>

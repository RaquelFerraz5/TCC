  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A síndrome de Guillain-Barré (SGB) é uma doença autoimune de etiologia (origem) não claramente conhecida. Caracterizada por comprometimento inflamatório agudo dos nervos periféricos e craniais, leva à debilidade simétrica progressiva e ascendente dos membros e tem variadas formas de evolução e complicações. É uma doença inflamatória reportada, geralmente, após uma infecção viral.</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
Não se sabe a causa específica da síndrome. No entanto, na maioria dos casos, duas ou três semanas antes, os portadores manifestaram uma doença aguda provocada por vírus (citomegalovírus, Epstein Barr, da gripe e da hepatite, por exemplo) ou bactérias (especialmenteCampylobacter jejuni ).  
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
A maioria dos pacientes percebe inicialmente a doença pela sensação de dormência ou queimação nas extremidades membros inferiores (pés e pernas) e, em seguida, superiores (mãos e braços). Dor neuropática lombar (nervos, medula da coluna ou no cérebro) ou nas pernas pode ser vista em pelo menos 50% dos casos. Fraqueza progressiva é o sinal mais perceptível ao paciente, ocorrendo geralmente nesta ordem: membros inferiores, braços, tronco, cabeça e pescoço.  
</p>
<p class="p">
Os sintomas principais da Síndrome de Guillain Barré são fraqueza muscular ascendente: começam pelas pernas, podendo, em seguida, progredir ou afetar o tronco, braços e face, com redução ou ausência de reflexos. A síndrome pode apresentar diferentes graus de agressividade, provocando leve fraqueza muscular em alguns pacientes ou casos de paralisia total dos quatro membros.  
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
A Síndrome de Guillain Barré corresponde a um grupo de polirradiculopatias autoimunes, inflamatórias e desmielinizantes. Os tratamentos de primeira linha constituem IGIV e plasmaférese. A IGIV costuma ser preferida principalmente em crianças por maior facilidade posológica e não envolver uso de dispositivos invasivos.   
</p>
<p>
O Sistema Único de Saúde (SUS) dispõe de tratamento para a síndrome de Guillain Barré, incluindo procedimentos, diagnósticos clínicos, de reabilitação e medicamentos. A Guillain Barré é uma doença rara e não é de notificação compulsória. O Brasil conta hoje com 136 Centros Especializados em Reabilitação, que atendem pacientes com a Síndrome de Guillain Barré pela rede pública de saúde.  </p>
<p class="p">
O SUS dispõe de Protocolo Clínico e Diretrizes Terapêuticas para a Síndrome de Guillain Barré, que prevê entre outros tratamentos, a disponibilidade do medicamento imunoglobolina intravenosa (IgIV) e do procedimento plasmaférese, que é uma técnica de transfusão que permite retirar plasma sanguíneo de um doador ou de um doente. O médico é o profissional responsável por indicar o melhor tratamento para o paciente, conforme cada caso.  

Não há necessidade de tratamento de manutenção fora da fase aguda da doença.
</p>

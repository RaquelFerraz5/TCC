<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A hiperidrose é doença caracterizada pelo excesso de produção de suor, que pode ser focal (localizada), envolvendo áreas específicas do corpo, ou generalizada, envolvendo todo o corpo. A hiperidrose generalizada pode ser fisiológica ou relacionada com disfunção autonômica secundária a desordens neurológicas, endocrinológicas, metabólicas, doenças febris, malignidades e drogas. A hiperidrose focal geralmente é localizada e simétrica, e acomete com maior frequência axilas, mãos, pés e face. As glândulas écrinas estão distribuídas por todo o corpo, com maior concentração nas regiões palmoplantares e na testa, e são inervadas por fibras colinérgicas do sistema nervoso simpático.  </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A causa mais provável da hiperidrose focal é a hiperexcitabilidade ou superatividade neurogênica dos circuitos reflexos envolvendo glândulas écrinas normais. Essa hiperexcitabilidade pode ser provocada por disfunção complexa das vias simpática e parassimpática dos sistemas autonômicos. Além disso, muitos pacientes apresentam resposta exagerada a estímulos físicos, emocionais e a aumentos da temperatura. 
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Suar excessivamente em diferentes pontos do corpo, como aquelas em que é é comum suar (axilas e pescoço, por exemplo) e em pontos onde o suor não é tão comum, como nas mãos, no rosto, nos pés e na virilha.
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Os sais de alumínio, o cloreto e o cloridrato são os tratamentos tópicos mais comuns para hiperidrose axilar. Usados em concentrações variáveis,mas geralmente não ultrapassando 20%,esses sais formam um complexo com mucopolissacarídeos e geram um precipitado que presumivelmente bloqueia os ductos epidérmicos ou promove atrofia e vacuolização das células glandulares.
</p>
<p class="p">
O uso de anticolinérgicos orais, como atropina e glicopirrolato, é limitado pela presença frequente de efeitos colaterais, como ressecamento oral e ocular, constipação e dificuldade para urinar. Estariam indicados, entretanto, nos casos que não respondem aos tratamentos tópicos, iontoforese ou toxina botulínica. </p>

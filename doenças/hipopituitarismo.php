<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  Hipopituitarismo congênito (HPC) é definido como a deficiência de um ou mais hormônios hipofisários. O HPC pode apresentar-se na forma de deficiência isolada do hormônio de crescimento (DIGH) ou combinada com a deficiência de outros hormônios hipofisários - deficiência hipotálamohipofisária múltipla (DHHM) e apresenta uma prevalência de um a cada 3.500 a 10.000 nascimentos. </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A deficiência de GH do adulto pode ser isolada ou associada a outras deficiências hormonais e decorrer de duas situações : persistência da deficiência de GH iniciada na infância; presença de lesão na região hipotálamo-hipofisária (tumor, irradiação do sistema nervoso central, trauma, doença inflamatória ou infecciosa) surgida na vida adulta. 
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
As manifestações da doenças podem incluir hipertensão, diabetes mellitus, resistência à insulina, apneia do sono, hirsutismo e hiperidrose. Além de sintomas aparentes como crescimento das extremidades, prognatismo frontal (crescimento em excesso da mandíbula), perda da visão e macroglossia (tamanho anormal da língua). 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Os principais achados clínicos em crianças e adolescentes com deficiência de GH são baixa estatura e redução da velocidade de crescimento. É importante salientar que outras causas de baixa estatura, como displasias esqueléticas, síndrome de Turner em meninas e doenças crônicas, devem ser excluídas. </p>

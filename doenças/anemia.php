  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A Anemia de Fanconi (AF) é uma alteração genética caracterizada por múltiplas anomalias congênitas, anormalidades hematológicas e predisposição a uma variedade de tumores. É uma doença causada por mutações em genes relacionados ao sistema de reparo. Devido a sua heterogeneidade gênica, o diagnóstico molecular dessa alteração é complexo. A variabilidade fenotípica desta síndrome dificulta o diagnóstico com base nas características clínicas, sendo necessários testes citogenéticos para sua confirmação, utilizando agentes alquilantes à cultura celular, que induzem a um aumento de quebras cromossômicas e à formação de figuras radiais características da doença.</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A anemia de Fanconi apresenta falha progressiva da medula óssea, podendo estar associada com uma variedade de anomalias congênitas. A insuficiência progressiva da medula óssea é a maior causa de morbidade e mortalidade dos pacientes com anemia de Fanconi. 
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Sintomas de apresentação: Lesão oral, Dor, Disfagia, Odinofagia, Perda de dentição, Úlcera, Massa pescoço, Hemorragia oral, Rouquidão. 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento do paciente com AF consiste em procedimentos paliativos e de suporte, os quais melhoram e prolongam suas condições de vida. Esses procedimentos incluem a reposição dos derivados do sangue, administração de andrógenos ou corticosteróides para estimular a medula óssea, o uso de antibióticos de amplo espectro e a prevenção à exposição a agentes que são potencialmente responsáveis por sangramento ou infecção. Além do transplante de medula óssea (TMO) que pode eliminar as manifestações hematológicas da doença que são as principais determinantes da sobrevida do paciente, independente do grupo de complementação. No entanto, os pacientes são suscetíveis às complicações associadas ao uso de ciclofosfamida (ZANIS-NETO et al., 1995), além da maioria não possuir doador compatível. </p>

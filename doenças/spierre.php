<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A primeira descrição histórica sobre a síndrome de Pierre Robin foi feita por Lannelongue e Menard em 1891, onde eles relataram 2 casos de pacientes com micrognatia, fenda palatina e retroglossoptose. Em 1923, Pierre Robin, um cirurgião-dentista francês, foi quem descreveu a tendência da queda da língua sobre a hipofaringe, causando obstrução das vias aéreas, assim como a fissura agravando os problemas dessas crianças. </p>



<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
  Até 1976, a tríade acima descrita era conhecida como Síndrome de Pierre Robin, entretanto, o termo síndrome passou a ser apenas utilizado para alterações na morfogênese com presença simultânea de múltiplas anormalidades causadas por uma única etiologia. Sendo assim, o termo sequência foi introduzido para incluir qualquer condição que envolva uma série de anormalidades causadas por uma cascata de eventos iniciados por uma única malformação. </p>
  <p>Dessa forma, a então conhecida Síndrome de Pierre Rubin passou a ser denominada como um complexo sintomático, não-específico, que pode ocorrer em várias situações isoladamente, em associação a alguma síndrome conhecida ou em associação com outros defeitos de desenvolvimento, que juntos não correspondem a uma síndrome específica. A literatura descreve a Sequência de Pierre Rubin (SPR) como uma tríade a anomalias caracterizadas por micrognatia, glossoptose e fissura de palato. Apesar da fissura estar presente na maioria dos casos, ela pode estar ausente em alguns. </p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
A expressão clínica da SR é muito heterogênea, variando desde leve dificuldade respiratória até graves crises de asfixia; os casos podem ser isolados ou associados a síndromes genéticas diferentes e com expressividade variável, porém a obstrução respiratória é fator comum a todos. Esta deve ser aliviada não só para melhorar as condições respiratórias, mas também as condições alimentares e nutricionais, de crescimento e desenvolvimento, visto que a gravidade do desconforto respiratório é tanto maior quanto menor a criança, e esta, quanto menor, mais susceptível a agravos respiratórios e nutricionais. 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Várias modalidades de tratamento são descritas na literatura: tratamento postural (a criança é colocada em posição prona), intubação nasofaríngea, glossopexia, traqueostomia e, mais recentemente, distração mandibular. Não existe, na literatura, um consenso sobre o tratamento da SR. Observa-se que a maioria dos estudos está sob o domínio das áreas de otorrinolaringologia e cirurgia craniofacial; várias técnicas cirúrgicas estão sendo desenvolvidas para aliviar a obstrução respiratória, como se a anomalia fosse somente um problema respiratório obstrutivo anatômico. Poucos estudos foram desenvolvidos para resolver as dificuldades alimentares, que se caracterizam por pouco volume de leite ingerido, alimentação por via oral (VO) demorada (geralmente maior que 30 minutos), fadiga, tosse, engasgos, vômitos e regurgitações durante e após as mamadas. Essas dificuldades podem levar à desnutrição protéico-calórica ou ao uso prolongado de sondas alimentadoras e suas graves conseqüências. 
</p>
<p class="p">
No Hospital de Reabilitação de Anomalias Craniofaciais da Universidade de São Paulo (HRAC/USP), vários estudos foram desenvolvidos com o objetivo de melhorar tanto as dificuldades respiratórias quanto as alimentares dos neonatos4-6, o que resultou numa visão dos problemas da SR diferente da maioria dos estudos publicados na literatura internacional: a SR não é somente um problema obstrutivo anatômico, mas também um problema de desenvolvimento, e deve estar sob o domínio de uma equipe multiprofissional. Assim, o objetivo deste artigo é apresentar protocolo único que possa atender tanto às dificuldades respiratórias como às dificuldades alimentares dos neonatos e lactentes com SR. </p>

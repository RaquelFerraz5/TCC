  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A hiperplasia adrenal congênita (HAC), também conhecida como hiperplasia congênita da suprarrenal (HCSR), é uma doença genética caracterizada por distúrbios no funcionamento das glândulas adrenais. As glândulas adrenais, ou suprarrenais, são duas glândulas que se localizam acima dos rins e produzem importantes hormônios para o organismo, como o cortisol e a aldosterona. </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
Por uma alteração nas enzimas necessárias ao funcionamento das glândulas adrenais, a pessoa com HAC não produz os hormônios adrenais na quantidade adequada ao organismo: 

Hormônios cortisol e aldosterona: produzidos em menor quantidade. Eles participam de várias funções importantes e são responsáveis pela manutenção do nível de glicose (açúcar) no sangue e pela conservação da água e sal no organismo, dentre outras funções. O cortisol é também o principal hormônio que atua na resposta ao estresse. 
</p>
<p class="p">
A HAC pode se apresentar em duas formas: clássica (grave) ou não clássica (leve). São dois os tipos de HAC clássica, geralmente diagnosticada logo após o nascimento ou na primeira infância: Perdedora de sal: As glândulas adrenais não conseguem produzir quantidades suficientes de cortisol e aldosterona. Sem o diagnóstico no tempo correto e o tratamento adequado, essa forma de HAC clássica pode levar à morte; Virilizante simples ou “não perdedora de sal”: As glândulas adrenais produzem quantidades suficientes de aldosterona, mas não suficientes de cortisol. 
</p>
<p class="p">

HAC não clássica: Ao contrário da HAC clássica, a HAC não clássica é leve e não ameaça a vida. Sinais e sintomas podem não aparecer na infância e nem na vida adulta.
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Os sinais e sintomas da hiperplasia adrenal (HAC) podem não se manifestar logo ao nascimento, o que dificulta o reconhecimento da doença. As manifestações clínicas na forma perdedora de sal, a mais grave, geralmente aparecem a partir da segunda semana de vida. Algumas delas são: Vômitos; Desidratação; Hipotensão (baixa pressão do sangue); Ganho inadequado ou mesmo perda de peso 
</p>
<p class="p">
Esse quadro é associado a alterações laboratoriais, dentre elas: Hiponatremia (menor concentração de sódio no sangue); Hiperpotassemia (grande concentração de potássio no sangue); Se não tratado adequadamente, esse conjunto de manifestações pode evoluir para o óbito. 
</p>
<p class="p">
Manifestações após o período neonatal, em crianças não tratadas: Virilização progressiva (desenvolvimento de características masculinas); Crescimento excessivo; Desenvolvimento ósseo avançado; Puberdade precoce; Baixa estatura na vida adulta. Devido ao excesso de hormônios andrógenos, provocado pela HAC, a criança do sexo feminino já pode nascer com a genitália externa virilizada. Nesse caso a criança deverá receber, imediatamente, atendimento especializado. 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Após o diagnóstico para a hiperplasia adrenal congênita (HAC), as crianças precisam ser tratadas e acompanhadas ao longo da vida. Elas devem passar por consultas frequentes, com periodicidade trimestral nos primeiros anos, e de forma mais espaçada posteriormente, quando o tratamento se estabiliza. 
</p>
<p class="p">
O tratamento da HAC tem por objetivo restituir a quantidade de hormônios que não estão sendo produzidos e consiste na reposição hormonal diária, via oral. Desta forma, busca-se evitar o desequilíbrio no nível de água e sal no organismo e a crise adrenal causada pela falta dos hormônios. Por outro lado, o tratamento controla as manifestações do excesso de andrógenos, evitando a virilização e preservando a altura final e a fertilidade. 
</p>
<p class="p">
Algumas medidas de suporte ao tratamento são o acréscimo de sal para os lactantes e o aumento do hormônio chamado glicocorticoide em situações de estresse. O tratamento precoce evita a desidratação grave e reduz o risco de morte. Além disso, as crianças do sexo feminino que apresentarem alteração na genitália externa são registradas com a informação correta do sexo. A correção cirúrgica da genitália deverá ser feita, quando necessária, preferencialmente entre os dois e seis meses de vida, por cirurgião experiente </p>

<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
Acromegalia é uma doença crônica causada pela produção e excreção excessiva do hormônio do crescimento (GH) na fase adulta, sendo mais comum entre os quarenta e cinquenta anos, pode ser identificado visualmente através do crescimento anormal das extremidades do corpo. Sua característica clínica se apresenta em fator do aumento do IGF-1 (fator de crescimento insulina-símile-1) ou somatomedina C. Um exame eficaz pode usar esse fator para identificar a doença.</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A grande maioria dos casos são decorrentes de tumores hipofisários benignos (tumores que atingem glândulas do corpo mas não se espalham para outras partes), geralmente um macroadenoma (grandes tumores).
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
As manifestações da doenças podem incluir hipertensão, diabetes mellitus, resistência à insulina, apneia do sono, hirsutismo e hiperidrose. Além de sintomas aparentes como crescimento das extremidades, prognatismo frontal (crescimento em excesso da mandíbula), perda da visão e macroglossia (tamanho anormal da língua). 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento da acromegalia tem a finalidade de reduzir os sintomas causados pela doença, amenizar o tamanho do tumor e reduzir a morbidade.
</p>
<p class="p">
A principal forma de tratamento é a cirurgia do tumor, mas quando a doença não pode ser controlada através disso, é indicado o uso de medicamentos como estrogênio transdérmico, ligantes dos receptores da somatostatina (como a octreotida e lanreotida), pasireotida, carbegolina e terapia com pegvisomanto.</p>

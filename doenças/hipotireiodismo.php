<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  O Hipotireoidismo Congênito (HC)  é  uma  síndrome  de  deficiência  de  hormônio  tireoidiano  em  recém nascidos  causadas  por  meio  de  um  desenvolvimento  incompleto  da  tireoide,  ocorrendo  dessa  forma  uma diminuição do hormônio tireoidiano ou da secreção do Hormônio Estimulante da Tireoide (TSH). Há tempos, o hipotireoidismo vem sendo uma questão social comum entre crianças e adultos em regiões com má nutrição, falta de assistência pré-natal e escassez de um diagnóstico precoce. O atraso do diagnóstico até o segundo ou terceiro mês do lactente, pode acabar por prejudicar o desenvolvimento cognitivo e físico do recém-nascido. </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
O teste do pezinho é um exame de rastreamento inserido no Programa de Triagem Neonatal que identifica doenças genéticas, metabólicas e hematológicas, como o Hipotireoidismo Congênito (HC). As causas incluem a não formação ou formação incompleta da glândula tireoide; Formação em local irregular da glândula tireoide; Defeitos na síntese dos hormônios da tireoide; Lesões na hipófise ou hipotálamo, que são duas glândulas no cérebro responsáveis pela produção e regulação dos hormônios.
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
A maioria dos pacientes encontravam-se assintomáticos, entretanto, quando presentes, os sintomas mais comuns foram: icterícia neonatal prolongada, choro rouco, letargia, movimentos lentos, constipação, macroglossia, hérnia umbilical, fontanelas amplas, hipotonia e pele seca. Portanto, a caracterização dos portadores de HC é de fundamental importância para a elaboração de medidas de amplificação do programa de triagem neonatal bem como na orientação de pais e cuidadores sobre esse agravo e seus sintomas.
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento do hipotireoidismo congênito consiste na reposição diária do hormônio tireoidiano (levo-tiroxina, L-T4), na forma de comprimido. A dose é individualizada, sendo maior para o recém-nascido e com possibilidade de ser diminuída com o avanço da idade. Normalmente é feito o uso de levotiroxina sintética.</p>

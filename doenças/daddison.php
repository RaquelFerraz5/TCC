<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A DA é uma doença crônica rara decorrente da lesão das glândulas adrenais, geralmente por destruição de 90% ou mais destas, por etiologias diversas e que culmina com uma deficiência de glicocorticoides, mineralocorticoides e andrógenos. DA é, portanto, a denominação dada a IA primária, já que a deficiência hormonal é devida ao comprometimento da própria glândula adrenal. </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A Doença de Addison é de etiologia variada podendo ser por infecção, tuberculose, causas genéticas ou até mesmo de origem auto-imune e é representada pela deficiência de glicocorticoides. 
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Sintomas inespecíficos como astenia, anorexia, perda de peso e distúrbios gastrointestinais (náuseas, vômitos, diarreia e constipação) são as manifestações mais comuns da DA, sendo os três primeiros presentes em 100% dos casos. A deficiência dos hormônios adrenais também pode acarretar dor abdominal, hipotensão (principalmente postural), avidez por sal, redução da libido e rarefação dos pelos axilares e pubianos apenas nas mulheres. 1 Uma manifestação mais específica da DA (não está presente na IA secundária) é a hiperpigmentação, presente em 92-94% dos pacientes. A hiperpigmentação decorre de uma maior concentração de melanina na pele pela elevação do ACTH e seu precursor, a pró-opiomelanocortina (POMC). 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento da insuficiência adrenal com a reposição hormonal de glico e mineralocorticoides é essencial para a manutenção da vida. A hidrocortisona, forma farmacológica genérica do cortisol, é a opção de escolha na reposição de glicocorticoides, por ser natural e ter atividade mineralocorticoide inerente. 3 A dose preconizada, atualmente, é de 15-25 mg/dia, dividida em duas ou três administrações. Um regime inicial típico seria hidrocortisona 10mg ao acordar, 5mg ao meio-dia e 5mg no início da noite. 1 No entanto, no Brasil não há disponibilidade de hidrocortisona oral e o tratamento é realizado com glicocorticoides sintéticos de ação intermediária, tipo prednisona e prednisolona, em dose de 5mg pela manhã e 2,5 mg à tarde. </p>

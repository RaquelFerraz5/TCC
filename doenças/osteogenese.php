<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A Osteogênese Imperfeita (OI) e os Tumores Ósseos, são doenças que se dão origem nos ossos, caracterizadas em causar a fragilidades e decorrentes fraturas. Os portadores destas doenças requerem cuidados de diversos profissionais, que buscam auxiliar melhor tratamento para os pacientes.</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
É uma doença rara e de díficil tratamento, essa condição possibilita fragilidades e fraturas nos ossos, na qual, acomete o desenvolvimento físico de seus portadores e impede o paciente de exercer atividades simples do seu dia a dia, ou até mesmo a sua locomoção. Pode ser causada por ordem genética ou pela falta de nutrientes necessários para os ossos. 
</p>
<p>A maioria dos casos é causada por alteração nos genes que codificam o colágeno tipo 1, COL1A1 e COL1A2. Outros genes que participam das modificações pós-traducionais, enovelamento da tripla hélice, crosslink, mineralização, desenvolvimento ósseo, diferenciação e maturação celular também já foram relacionados com OI demonstrando a heterogeneidade genética desta condição.
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Os sintomas variam muito conforme o tipo adquirido da doença, mas as principais características apresentadas foram: deformidades físicas, fraturas internas, ossos frágeis, perda de audição e parte branca do olho azulada.  
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Os casos podem ser leves ou letais, com alta deformidade fisiológica, não há tratamento curativo mas a suplementação de cálcio, vitamina D e tratamento com bifosfonatos procuram reduzir o número de fraturas destes pacientes, além da recomendação de fisioterapia para alguns casos.  </p>

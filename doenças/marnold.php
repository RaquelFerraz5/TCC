  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  A Síndrome de Arnold-Chiari é uma malformação rara que acomete o sistema nervoso central do paciente, gerando dificuldades de equilíbrio, perda de coordenação e problemas visuais, por exemplo. </p>
  <p class="p">
  Essa malformação, que pode ser genética e ocorre ainda no desenvolvimento do feto, tem como principal característica o desenvolvimento inadequado do cerebelo – parte do cérebro responsável pelo equilíbrio –, e pode ser classificada em quatro tipos principais: 
</p>
  <p class="p">
  <li class="li">Chiari I: é o tipo mais comum de ser observado e ocorre quando o cerebelo estende-se até o fim de um orifício presente na base do crânio, denominado forame magno, onde geralmente deve passar a medula espinhal, o que pode acabar pressionando o tronco cerebral, o canal espinhal e obstruindo o fluxo de fluido; </li>

    <li class="li">Chiari II: além do cerebelo, igualmente atinge o tronco encefálico, e ambos se estendem para o forame magno. Esse tipo é mais comumente observado em crianças que tenham espinha bífida, ou seja, uma falha no desenvolvimento da medula espinhal e das estruturas que a protegem; </li>

    <li class="li">Chiari III: ocorre quando o cerebelo e o tronco encefálico ultrapassam o limite do forame magno e atingem a medula espinhal, sendo esse o tipo mais grave da doença. </li>

    <li class="li">Chiari IV: pode ser considerado o mais raro por não ser compatível com a vida, ou seja, ocorre quando não tem desenvolvimento ou há um desenvolvimento incompleto do cerebelo. </li>
</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
Por não se ter uma origem exata, a Síndrome de Arnold-Chiari ainda divide opiniões e costuma ser contestada. Alguns médicos acreditam que a enfermidade deriva de uma desordem na circulação do líquido cefalorraquidiano (LCR); outros acreditam que se dá pelo tamanho inferior da fossa posterior, onde se encontra o cerebelo. 
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Devido ao fato de estar diretamente relacionada à manutenção do equilíbrio, a síndrome que acomete a fossa posterior da cabeça costuma causar distúrbios dessa ordem. Porém, a perda de estabilidade é apenas um dos sintomas que acompanham os pacientes. Outras manifestações que podem ser percebidas são: Visão turva ou dupla, e sensibilidade à luz; Alterações dos reflexos e da coordenação motora; Vertigens recorrentes; Dores de cabeça e no pescoço; Dificuldade na deglutição acompanhada de náuseas; Gagueira; Dormência ou formigamento nos pés e nas mãos; Incontinência urinária; Dificuldade na fala; Arqueamento ou curvatura da coluna relacionada ao comprometimento da medula espinhal; Problemas relacionados às habilidades motoras finas; Fraqueza muscular; Zumbido ou perda auditiva; Dificuldade para dormir; Tontura; Depressão; Problemas respiratórios, tais como a apneia do sono.
</p>
<p class="p">Mas algumas crianças podem não apresentar sinais ou sintomas, e consequentemente nem devem necessitar de um tratamento para a condição, que costuma ser detectada após a realização de testes para distúrbios não relacionados. </p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
Quanto ao tratamento, a melhor abordagem depende de forma, gravidade e sintomas associados, mas costuma envolver monitoramento regular, fazer uso de medicamentos e se submeter a uma cirurgia, ou até mesmo nenhuma intervenção. 

É comum que os pacientes com suspeita desta malformação sejam submetidos a exames de ressonância nuclear magnética, que capta imagens capazes de determinar a junção do crânio com a região cervical, possibilitando o planejamento do melhor tipo de intervenção. 
  </p>
<p class="p">
Se for constatado que a junção entre as regiões está ocasionando uma deterioração do tecido cerebral com progressão e agravamento dos sintomas, a intervenção cirúrgica, como a descompressão, pode ser o meio de tratamento mais adequado. 

Já aqueles pacientes infantis que não sofrem alterações anatômicas severas na estrutura cerebral podem se recuperar por meio do uso de medicamentos controlados, que ajudam a amenizar os sintomas presentes nesta Síndrome. 

É importante ressaltar que apenas os médicos especialistas em Neuropediatria ou Neurocirurgia podem oferecer a melhor opção de tratamento para cada caso infantil, assim como confirmar o diagnóstico da malformação de Chiari. </p>

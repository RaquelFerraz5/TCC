<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  Autismo e condições relacionadas (agora amplamente conhecidos como transtornos de espectro autista, ou TEAs) são transtornos que compartilham déficits significativos na interação social como sua principal característica definidora. Esse déficit social e bastante severo, e sua gravidade e seu inico precoce levam a mais problemas gerais e disseminados tanto na aprendizagem como na adaptação. </p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
A causa ou causas específicas do autismo são ainda desconhecidas, sabe-se, contudo que tem uma base genética importante. Sobre esta determinante genética seriam acumulados fatores adicionais (do meio interno e/ou envolvente) que eventualmente poderiam levar ao autismo e que seguramente contribuem para a sua expressão.  
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Dificuldades no desenvolvimento social são os indicadores mais prováveis de um futuro diagnóstico de autismo, entretanto o atraso da fala aparece ser o motivo que mais mobiliza os pais na busca por assistência. Falar das características do autismo obriga a falar-se de Leo Kanner. Foi este que, há cerca de meio seculo, publicou num artigo famoso, onde faz uma descrição sobre crianças cujos comportamentos lhe pareciam ser diferentes de todos os outros até estão destacados pela literatura científica internacional. 

    <li class="li">Um profundo afastamento autista; </li>

    <li class="li">Um desejo autista pela conservação da semelhança; </li>

    <li class="li">Uma boa capacidade de memorização mecânica;</li> 

    <li class="li">Expressão inteligente e ausente; </li>

    <li class="li">Mutismo ou linguagem sem intenção comunicativa efetiva; </li>

    <li class="li">Hipersensibilidade aos estímulos; </li>

    <li class="li">Relação estranha e obsessiva com objetos. </li>
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
A cura para o autismo não existe e o tratamento do autismo não se prende a uma única terapêutica. O uso de medicamento que antes desempenhava um papel fundamental e de grande importância no tratamento (devido á crença da relação do autismo com os quadros psicóticos do adulto), passa a ter uma função de apenas aliviar os sintomas do autista para que outra abordagem, como a reabilitação e a educação especial, possam se adaptadas e tenham resultados eficazes. </p>

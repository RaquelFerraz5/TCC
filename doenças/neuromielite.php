<div class="bloco" id="cont">
  <h2 class="texto1" style="color:#004e3de0;">O que é?</h2>
  <p class="p"> 
  É uma doença imunomediada (que agride o próprio sistema de defesa do corpo) no Sistema Nervoso Central (encéfalo e medula espinhal), mais frequentemente encontrado nas mulheres, sendo cerca de 10 casos em mulheres para cada homem com a doença, causando a neurite óptica grave, a mielite transversa longitudinal extensa e a síndrome de área posterior do bulbo.</p>

 

<h2 class="texto1" style="color:#004e3de0;">Causas</h2>

 
<p class="p">
O que causa essa doença é o defeito na presença de um anticorpo específico, a aquaporina 4, que desencadeia neuroinflamações (inflamação no tecido nervoso central), lesões longitudinais (que afetam a movimentação), lesões nos nervos ópticos e quiasma óptico.
</p>


<h2 class="texto1" style="color:#004e3de0;">Sintomas </h2>

<p class="p">
Alguns dos sintomas incluem náuseas, vômitos e soluços recorrentes. Esse mal estar aparece de forma intensa em certos períodos, sempre de forma intermitente. Além de dor ocular; perda parcial ou total da visão; dor nas costas que alcança a parte inferior do peito, circunda o tórax e o abdômen e irradia pelas pernas e braços; sensações anormais de dormência, formigamento, frio e queimação; rigidez, aperto, contrações e espasmos da musculatura.
</p>
<p class="p">
O diagnóstico é baseado nos critérios do Painel Internacional para Diagnóstico NMO, que definiu os critérios diagnósticos atuais em 2015 a partir dos sintomas clínicos típicos e na positividade ou não do anticorpo anti-aquaportina-4. 
</p>
 

<h2 class="texto1" style="color:#004e3de0;">Tratamento</h2>

 
<p class="p">
O tratamento das dores agudas é realizado com corticoterapia endovenosa de 3-5 dias associado ao uso de plamaférese em casos graves ou que o tratamento com corticoide não se mostra eficiente. Em estudos recentes o uso de Azapriotina, Rituximab ou Micofenolato também foram utilizados no tratamento da doença com certa eficácia.</p>
